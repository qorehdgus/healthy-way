package com.team1.health.vo;

public class IngredientVO {
	private double gred_kcal;
	private String gred_name;
	private String gred_type;
	private String gred_num;
	
	public double getGred_kcal() {
		return gred_kcal;
	}
	public void setGred_kcal(double gred_kcal) {
		this.gred_kcal = gred_kcal;
	}
	public String getGred_name() {
		return gred_name;
	}
	public void setGred_name(String gred_name) {
		this.gred_name = gred_name;
	}
	public String getGred_type() {
		return gred_type;
	}
	public void setGred_type(String gred_type) {
		this.gred_type = gred_type;
	}
	public String getGred_num() {
		return gred_num;
	}
	public void setGred_num(String gred_num) {
		this.gred_num = gred_num;
	}	
}
